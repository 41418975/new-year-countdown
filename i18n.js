class I18nManager {
    constructor() {
        this.currentLanguage = 'zh-CN';
        this.storageKey = 'countdownLanguage';
        this.init();
    }

    init() {
        const savedLang = localStorage.getItem(this.storageKey);
        if (savedLang && i18n[savedLang]) {
            this.currentLanguage = savedLang;
        } else {
            this.detectLanguage();
        }
    }

    detectLanguage() {
        const browserLang = navigator.language || navigator.userLanguage;
        const langMap = {
            'zh': 'zh-CN',
            'zh-CN': 'zh-CN',
            'zh-TW': 'zh-CN',
            'zh-HK': 'zh-CN',
            'en': 'en-US',
            'en-US': 'en-US',
            'en-GB': 'en-US',
            'ja': 'ja-JP',
            'ja-JP': 'ja-JP',
            'ko': 'ko-KR',
            'ko-KR': 'ko-KR'
        };
        
        this.currentLanguage = langMap[browserLang] || 'zh-CN';
    }

    setLanguage(lang) {
        if (i18n[lang]) {
            this.currentLanguage = lang;
            localStorage.setItem(this.storageKey, lang);
            return true;
        }
        return false;
    }

    getLanguage() {
        return this.currentLanguage;
    }

    getAvailableLanguages() {
        return Object.keys(i18n).map(key => ({
            code: key,
            name: i18n[key].languageName,
            flag: i18n[key].languageFlag
        }));
    }

    t(key, params = {}) {
        const keys = key.split('.');
        let value = i18n[this.currentLanguage];
        
        for (const k of keys) {
            if (value && typeof value === 'object' && k in value) {
                value = value[k];
            } else {
                return key;
            }
        }
        
        if (typeof value === 'string') {
            return this.interpolate(value, params);
        }
        
        return value;
    }

    interpolate(text, params) {
        return text.replace(/\{(\w+)\}/g, (match, key) => {
            return params[key] !== undefined ? params[key] : match;
        });
    }

    updateAllText() {
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            const text = this.t(key);
            if (text !== key) {
                el.textContent = text;
            }
        });

        document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
            const key = el.getAttribute('data-i18n-placeholder');
            const text = this.t(key);
            if (text !== key) {
                el.placeholder = text;
            }
        });

        document.querySelectorAll('[data-i18n-title]').forEach(el => {
            const key = el.getAttribute('data-i18n-title');
            const text = this.t(key);
            if (text !== key) {
                el.title = text;
            }
        });

        document.querySelectorAll('select option[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            const text = this.t(key);
            if (text !== key) {
                el.textContent = text;
            }
        });

        this.updateDynamicText();
    }

    updateDynamicText() {
        const siteTitle = document.getElementById('siteTitle');
        if (siteTitle) {
            siteTitle.textContent = this.t('siteTitle');
        }
    }
}

